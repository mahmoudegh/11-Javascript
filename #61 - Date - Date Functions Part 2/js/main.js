//In All Functions WE Can Add UTC After get To Convert To Universal Time.

///////////////////////////// #01 - setDate(dd); ///////////////////////////////

var theDate = new Date();

theDate.setDate(10);

console.log(theDate);



/////////////////////// #02 - setFullYear(yy-mm-dd); ///////////////////////////

var theDate = new Date();

theDate.setFullYear(2000, 10, 20);

console.log(theDate);



//////////////////////// #03 - setHours(hh-mm-ss-ms); //////////////////////////

var theTime = new Date();

theTime.setHours(22, 23, 24, 3600000);

console.log(theTime);



////////////////////// #04 - setMinutes(mm-ss-ms); /////////////////////////////

var theTime = new Date();

theTime.setMinutes(23, 24, 3600000);

console.log(theTime);



////////////////////////// #05 - setSeconds(ss-ms); ////////////////////////////

var theTime = new Date();

theTime.setSeconds(23, 3600000);

console.log(theTime);



///////////////////////// #06 - setMilliseconds(ms); ///////////////////////////

var theTime = new Date();

theTime.setMilliseconds(3600000);

console.log(theTime);



/////////////////////////// #07 - setMonth(mm-dd); /////////////////////////////
var theDate = new Date();

theDate.setMonth(11, 29);

console.log(theDate);






































































